package com.jike.cashocean.model;


public class ContactsData {
    /**
     * name : 联系人姓名
     * phone : 手机号
     */

    private String name;
    private String phone;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

}
